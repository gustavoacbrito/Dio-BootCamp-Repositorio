package ClassesAprendizados.Calculadora;


public class Calculadora {



    public static void handleSum(double number1, double number2){
        double result = number1 + number2;
        System.out.println(number1 + " + " + number2 + " = " + result);
    }
    public static void handleSubtraction(double number1, double number2){
        double result = number1 - number2;
        System.out.println(number1 + " - " + number2 + " = " + result);
    }
    public static void handleMultiplication(double number1, double number2){
        double result = number1 * number2;
        System.out.println(number1 + " x " + number2 + " = " + result);
    }
    public static void handleDivision(double number1, double number2){
        double result = number1 / number2;
        System.out.println(number1 + " รท " + number2 + " = " + result);
    }




}
